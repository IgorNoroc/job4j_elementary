package ru.job4j.calculator;

/**
 * Addition of two numbers.
 *
 * @author Igor Noroc (workuglis@gmail.com)
 */
public class Calculator {
    /**
     * Main.
     * @param args - args.
     */
    public static void main(String[] args) {
        int one = 1;
        int two = 2;
        int result = one + two;
        System.out.println(result);
        one = 10;
        two = 11;
        result = one + two;
        System.out.println(result);
    }
}
