package ru.job4j.condition;

/**
 * 0. Операции сравнения[#257408]
 */
public class Liken {
    /**
     * Main.
     *
     * @param args
     */
    public static void main(String[] args) {
        int first = 10;
        int second = 9;

        boolean result = first > second;
        System.out.println(result);

        result = first < second;
        System.out.println(result);

        result = first == second;
        System.out.println(result);
    }
}
