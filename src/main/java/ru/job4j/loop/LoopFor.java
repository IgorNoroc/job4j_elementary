package ru.job4j.loop;

/**
 * 5.0. Цикл for. Определение.[#257492]
 */
public class LoopFor {
    /**
     * Main.
     * Вывод индексов в консоль.
     *
     * @param args
     */
    public static void main(String[] args) {
        for (int index = 5; index <= 10; index++) {
            System.out.println(index);
        }
    }
}
